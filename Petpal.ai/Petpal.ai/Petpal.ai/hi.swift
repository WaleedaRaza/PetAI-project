import Foundation
Check Build Settings:
In Xcode, select the Petpal.ai project in the navigator (top-left).
Click the Petpal.ai target under "Targets".
Go to the "Build Settings" tab.
Search for Info.plist File.
Ensure it’s set to Petpal.ai/Info.plist. If not, double-click and set it to Petpal.ai/Info.plist.
Add Info.plist to Project: If the file isn’t in the project navigator:
Right-click the Petpal.ai group in the navigator > "Add Files to Petpal.ai".
Select Info.plist from Petpal.ai/Petpal.ai/.
Ensure "Copy items if needed" is checked and the file is added to the target.
